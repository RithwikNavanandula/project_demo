# inferBatch returns (list_of_preds, something_else)
# → take index [0] to get predictions, then each pred is a list → take [0] again
class _Batch:
    def __init__(self, imgs): self.imgs = imgs

def run_batch(imgs_proc):
    result = model.inferBatch(_Batch(imgs_proc))
    preds  = result[0]                          # unpack tuple → list of preds
    return [p[0].strip() if isinstance(p, list) else p.strip() for p in preds]

# ── Inference loop ───────────────────────────────────────────────
all_preds, all_gts = [], []
cer_sum = wer_sum = 0.0
exact_count = 0

for start in tqdm(range(0, total, BATCH_SIZE)):
    batch_items = eval_data[start : start + BATCH_SIZE]
    imgs_proc   = [preprocess(np.array(img.convert("L")), Model.imgSize)
                   for img in batch_items["image"]]
    preds       = run_batch(imgs_proc)

    for pred, gt in zip(preds, batch_items["text"]):
        gt = gt.strip()
        all_preds.append(pred)
