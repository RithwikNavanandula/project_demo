print(f"  {'Words':>8}  {'Samples':>8}  {'SER%':>8}  {'CER%':>8}")
print(f"  {'─'*40}")
df["length_bin"] = pd.cut(df.word_count,
                           bins=[0,3,6,9,12,50],
                           labels=["1-3","4-6","7-9","10-12","13+"])
length_groups = df.groupby("length_bin", observed=True)
for grp, sub in length_groups:
    print(f"  {str(grp):>8}  {len(sub):>8}  "
          f"{sub.ser.mean()*100:>7.1f}%  {sub.cer.mean():>7.2f}%")

