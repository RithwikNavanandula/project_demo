    n = len(indices)
    if n == 0:
        print("  (no samples to show)")
        return

    fig, axes = plt.subplots(n, 1, figsize=(14, 3.2 * n))
    if n == 1:
        axes = [axes]
    fig.suptitle(title, fontsize=11, fontweight="bold")   # ASCII title — default font

    for ax, idx in zip(axes, indices):
        row = df.iloc[idx]
        img = eval_data[int(row.idx)]["image"]
        ax.imshow(img, aspect="auto")
        ax.axis("off")

        color = "#1a7a1a" if row.exact else "#b22222"

        # ── Latin metadata above image using default font ─────────
        ax.text(0, -8,
                f"CER: {row.cer:.1f}%  |  font: {row.font}  |  size: {row.font_size}",
                transform=ax.transData, fontsize=8,
                color=color, va="bottom")

        # ── Hindi GT & PRED below image using Devanagari font ─────
        img_h = np.array(img).shape[0]
        ax.text(0, img_h + 4,
                f"GT:   {row.gt}",
                transform=ax.transData,
                fontproperties=hindi_fp, fontsize=9,
                color="#1a7a1a", va="top")
        ax.text(0, img_h + 22,
                f"PRED: {row.pred}",
                transform=ax.transData,
                fontproperties=hindi_fp, fontsize=9,
                color=color, va="top")

    plt.tight_layout()
    plt.show()

